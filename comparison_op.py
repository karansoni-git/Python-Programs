# Comparison operators are used to compare two values or expressions. The result is always a Boolean value: True or False.

x = 15
y = 20

print("x == y : " , x == y)
print("x != y : " , x != y)
print("x > y : " , x > y)
print("x < y : " , x < y)
print("x >= y : " , x >= y)
print("x <= y : " , x <= y)