# \\	Backslash (\)
print("this is \\backslash")

# \'	Single quote (')
print('single \'quote\'')

# \"	Double quote (")
print("double \"quote\"")

# \n	New line
print("pythong is\nrunning")

# \t	Horizontal tab
print("hit \tthe code")

# \r	Carriage return
print("sample \rtesting")

# \b	Backspace
print("hello\bboy")

# \f	Form feed
print("next\fpage")

# \a	Bell/alert (makes a beep sound in some terminals)
print("\amake a beep sound")

# \v	Vertical tab
print("give me \vsome space")
