# Logical operators are used to combine multiple conditional expressions. They return a Boolean value: True or False.

x = 10
y = 5

# AND
print(x > 5 and y < 10)   # True and True → True

# OR
print(x > 15 or y < 10)   # False or True → True

# NOT
print(not(x > 5))         # not(True) → False
